#La funcion Print nos sirve para mostrar un mensaje por consola 

print(2 + 3) # Adición(+) 
print(3 - 1) # Resta(-)
print(3*2) # Multiplicación 
print(3/2) # División (/)
print(3 % 2) # módulo(%) el modulo devuelve la parte entera de la division de cirtos numeros 
print(3 ** 2) # Operaciones Exponenciales Ej: 2^2
print(3 // 2 ) #Division entera sin dejar residuo 

# Comprobacion de tipos de datos 
#Para comprobar de que tipo es la variable o el dato almacenado usamos la funcion type junto a print para mostrar el tipo de dato en consola 

print(type(10)) # Int 
print(type(3.14)) # float 
print(type(3 + 3j)) # Operación compleja
print(type("Lorem")) # String 
print(type([1,2,3])) # Lista 
print(type({"Nombre" : "Loremp"})) # Dicionario 
print(type({9.8,3.14,2.7})) # colocar 
print(type(9.8,3.14,2.7)) # Tupla 
print(type(True)) # Boolean 
# Definicion de tipos de datos 

